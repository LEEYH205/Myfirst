class BlockComment
{
	public static void main(String[] args)
	{
		/* 다음은 단순한 정수의 출력 */
		System.out.println(7);
		System.out.println(3.15);
		System.out.println(3 + 5);
		System.out.println("3 + 5" + " 의 연산 결과는 8입니다.");
		System.out.println("3 + 5 = " + 8);
		
		/* 다음은 덧셈 결과의 출력 */
		System.out.println(3.15 + "는 실수입니다.");
	}
}